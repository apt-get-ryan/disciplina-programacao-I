public class Principal003 {

    public static void main(String[] args) {
        int numero = 6;

        numero += 2; // numero = numero + 2;
        
        System.out.println(numero);

        numero -= 2; // numero = numero - 2;

        System.out.println(numero);

        numero *= 2; // numero = numero - 2;

        System.out.println(numero);

        numero /= 2; // numero = numero - 2;

        System.out.println(numero);
        
        numero %= 2; // numero = numero - 2;

        System.out.println(numero);
    }
}